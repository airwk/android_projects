public final class Color3
{
    public float r, g, b;

    public Color3()
    {

    }

    public Color3(float r, float g, float b)
    {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public void set(float r, float g, float b)
    {
        this.r = r;
        this.g = g;
        this.b = b;
    }
}